// example institute (static variable declaration,iitialization and access)....-p-p
class example{
	static char name = 'N'; // static variable declaration and initialization...
	static byte strength = 10;
	static short a = 5;
	static int b = 6;
	static float c = 1.123456789123456f;
	static double e = 1.123456789123456789d;
	static boolean d = false;
	public static void main(String [] anand){

		System.out.println(name); // calling static variable in main() method.....
		System.out.println(strength);
		System.out.println(a);
		System.out.println(b);
		System.out.println(c);
		System.out.println(e);
		System.out.println(d);
}

}

